			<h3>Selamat Datang di Lembaga Kesejahteraan Sosial Anak FAJAR HARAPAN</h3>
			<p>Lembaga Kesejahteraan Sosial Anak adalah salah satu amal usaha Muhammadiyah yang memberikan santunan kepada anak-anak keluarga yang tidak mampu, diutamakan anak-anak yatim agar mereka berkemampuan hidup dalam masyarakat yang penuh persaingan serta memiliki kepribadian Islam.</p>
			<p><b><u>Fungsi LKS</u></b></p>
			<p>
				<ol>
					<li><b>Pemulihan,</b>yaitu mengembalikan kondisi fisik, menatl dan sosial anak asuh pada kondisi yang semestinya dimiliki oleh anak pada umumnya.</li>
					<li><b>Perlindungan,</b>yaitu melindungi anak asuh dari gangguan fisik, mental dan sosial dari pengaruh luar.</li>
					<li><b>Pembinaan,</b>yaitu membina anak asuh agar tercegah dari pengaruh negatif.</li>
					<li><b>Pengembangan,</b>yaitu mengembangkan sumber daya yang ada pada anak asuh untuk diarahkan kepada peningkatan pemahaman tentang Dienul Islam, intelektualitas dan ketrampilan sebagai bekal bagi kehidupan kelak.</li>
				</ol>
			</p>
